#!/usr/bin/env python3
"""
Basic annotations - to string

Write a type-annotated function 'to_str' that takes a float argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Parameters:
        n (float): The number to convert to a string.

    Returns:
        str: The string representation of n.
    """
    return str(n)
